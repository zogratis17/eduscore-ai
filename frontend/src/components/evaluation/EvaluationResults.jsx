import React, { useState, useEffect } from 'react';
import { useParams, useNavigate } from 'react-router-dom';
import { evaluationService } from '../../services/evaluationService';
import { ArrowLeft, Download, FileText, CheckCircle, AlertTriangle } from 'lucide-react';
import Button from '../common/Button';
import LoadingSpinner from '../common/LoadingSpinner';
import Card from '../common/Card';

export default function EvaluationResults() {
  const { id } = useParams();
  const navigate = useNavigate();
  const [results, setResults] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const [downloading, setDownloading] = useState(false);

  useEffect(() => {
    loadResults();
  }, [id]);

  const loadResults = async () => {
    try {
      setLoading(true);
      const data = await evaluationService.getResults(id);
      setResults(data);
    } catch (err) {
      console.error(err);
      setError("Could not load evaluation results. Please try again later.");
    } finally {
      setLoading(false);
    }
  };

  const handleDownload = async () => {
    try {
      setDownloading(true);
      await evaluationService.downloadReport(id);
    } catch (err) {
      console.error(err);
      alert("Failed to download report");
    } finally {
      setDownloading(false);
    }
  };

  if (loading) return <div className="flex justify-center p-12"><LoadingSpinner /></div>;
  if (error) return <div className="p-8 text-red-500 text-center">{error}</div>;
  if (!results || results.status === "processing") {
    return (
      <div className="text-center p-12">
        <h2 className="text-xl font-semibold mb-4">Analysis in Progress</h2>
        <p className="text-gray-500 mb-6">Our AI is currently analyzing your document. This usually takes less than a minute.</p>
        <Button onClick={loadResults} variant="outline">Refresh Status</Button>
      </div>
    );
  }

  // Calculate colors based on score
  const getScoreColor = (score) => {
    if (score >= 80) return "text-green-600";
    if (score >= 60) return "text-yellow-600";
    return "text-red-600";
  };

  return (
    <div className="max-w-5xl mx-auto p-6">
      <Button 
        variant="ghost" 
        onClick={() => navigate('/dashboard')} 
        className="mb-6 flex items-center gap-2 text-gray-600"
      >
        <ArrowLeft size={16} /> Back to Dashboard
      </Button>

      <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
        {/* Main Score Card */}
        <Card className="md:col-span-2 p-8">
          <div className="flex justify-between items-start mb-6">
            <div>
              <h1 className="text-2xl font-bold text-gray-900">Evaluation Report</h1>
              <p className="text-gray-500 text-sm mt-1">Generated by EduScore AI</p>
            </div>
            <div className="text-right">
              <div className={`text-5xl font-bold ${getScoreColor(results.final_score)}`}>
                {results.final_score}
              </div>
              <div className="text-gray-400 text-sm font-medium mt-1">out of 100</div>
              <div className="text-2xl font-bold text-gray-800 mt-2">Grade: {results.grade}</div>
            </div>
          </div>

          <div className="prose max-w-none text-gray-700">
            <h3 className="text-lg font-semibold text-gray-900 mb-3">Overall Feedback</h3>
            <p className="leading-relaxed bg-gray-50 p-4 rounded-lg border border-gray-100">
              {results.overall_feedback}
            </p>
          </div>

          <div className="mt-8 flex justify-end">
            <Button 
              onClick={handleDownload} 
              disabled={downloading}
              className="flex items-center gap-2"
            >
              {downloading ? <LoadingSpinner size="sm" /> : <Download size={18} />}
              Download PDF Report
            </Button>
          </div>
        </Card>

        {/* Breakdown Column */}
        <div className="space-y-6">
          <Card className="p-6">
            <h3 className="font-semibold text-gray-900 mb-4 flex items-center gap-2">
              <CheckCircle className="text-blue-500" size={20} />
              Component Scores
            </h3>
            <div className="space-y-4">
              <ScoreRow label="Grammar & Style" score={results.components?.grammar?.score} />
              <ScoreRow label="Vocabulary" score={results.components?.vocabulary?.score} />
              <ScoreRow label="Coherence" score={results.components?.coherence?.score} />
            </div>
          </Card>

          <Card className="p-6">
            <h3 className="font-semibold text-gray-900 mb-4 flex items-center gap-2">
              <AlertTriangle className="text-orange-500" size={20} />
              Plagiarism Check
            </h3>
            {results.components?.plagiarism ? (
              <div>
                <div className="flex justify-between mb-2">
                  <span className="text-gray-600">Similarity</span>
                  <span className={`font-bold ${results.components.plagiarism.percentage > 15 ? 'text-red-500' : 'text-green-600'}`}>
                    {results.components.plagiarism.percentage}%
                  </span>
                </div>
                <div className="w-full bg-gray-200 rounded-full h-2.5">
                  <div 
                    className={`h-2.5 rounded-full ${results.components.plagiarism.percentage > 15 ? 'bg-red-500' : 'bg-green-500'}`} 
                    style={{ width: `${Math.min(results.components.plagiarism.percentage, 100)}%` }}
                  ></div>
                </div>
                <p className="text-xs text-gray-500 mt-3">
                  Compared against internal database of {results.components.plagiarism.matches?.length || 0} documents.
                </p>
              </div>
            ) : (
              <p className="text-sm text-gray-500">Analysis unavailable</p>
            )}
          </Card>
        </div>
      </div>
    </div>
  );
}

function ScoreRow({ label, score }) {
  const numScore = parseFloat(score || 0);
  return (
    <div>
      <div className="flex justify-between mb-1 text-sm">
        <span className="font-medium text-gray-700">{label}</span>
        <span className="text-gray-900">{numScore.toFixed(1)}/100</span>
      </div>
      <div className="w-full bg-gray-200 rounded-full h-2">
        <div 
          className="bg-blue-600 h-2 rounded-full" 
          style={{ width: `${Math.min(numScore, 100)}%` }}
        ></div>
      </div>
    </div>
  );
}
